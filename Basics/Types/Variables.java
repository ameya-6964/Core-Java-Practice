//! Variables :- Temporary Store Data In Computers Memory
package Basics.Types;



public class Variables {
    public static void main(String[] args) {
        //! Syntax :- variableType Identifier = literal/Value
        //! Syntax :- int a = 10;
        byte a = 100;
        System.out.println(a);

        //! short
        short b = 70;
        System.out.println(b);

        //! int
        int c = 10;
        System.out.println(c);

        //! long
        long d = 10000000;
        System.out.println(d);

        //! float
        float e = 3.14f;
        System.out.println(e);

        //! double
        double f = 2.131313131d;
        System.out.println(f);

        //! char
        char g = '2';
        System.out.println(g);

        //! boolean
        boolean flag = false;
        System.out.println(flag);

    }
}
